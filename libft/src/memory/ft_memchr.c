#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	t_byte	*src;

	src = (t_byte *)s;
	while (n--)
	{
		if (*src == (t_byte)c)
			return (src);
		src++;
	}
	return (NULL);
}
